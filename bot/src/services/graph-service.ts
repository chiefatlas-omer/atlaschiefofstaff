// --- Finders ---

export function findPersonBySlackId(slackUserId: string) {
  return db.select().from(people).where(eq(people.slackUserId, slackUserId)).get() ?? null;
}

export function findPersonByEmail(email: string) {
  return db.select().from(people).where(eq(people.email, email)).get() ?? null;
}

export function findPersonByName(name: string) {
  return db.select().from(people).where(eq(people.name, name)).get() ?? null;
}

