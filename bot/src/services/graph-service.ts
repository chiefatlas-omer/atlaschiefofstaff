// --- findOrCreate helpers ---

export function findOrCreatePerson(input: CreatePersonInput) {
  // Try slackUserId first
  if (input.slackUserId) {
    const existing = findPersonBySlackId(input.slackUserId);
    if (existing) return existing;
  }

  // Try email
  if (input.email) {
    const existing = findPersonByEmail(input.email);
    if (existing) return existing;
  }

  // Try exact name match
  if (input.name) {
    const existing = findPersonByName(input.name);
    if (existing) return existing;
  }

  // Create new
  return createPerson(input);
}

export function findOrCreateCompany(input: CreateCompanyInput) {
  const existing = findCompanyByName(input.name);
  if (existing) return existing;
  return createCompany(input);
}

// --- Relationship management ---

export function linkEntities(
  sourceType: string,
  sourceId: string,
  targetType: string,
  targetId: string,
  relationshipType: string,
  metadata?: Record<string, unknown>,
) {
  // Prevent duplicates
  const existing = db.select().from(relationships).where(
    and(
      eq(relationships.sourceType, sourceType),
      eq(relationships.sourceId, sourceId),
      eq(relationships.targetType, targetType),
      eq(relationships.targetId, targetId),
      eq(relationships.relationshipType, relationshipType),
    )
  ).get();

  if (existing) return existing;

  const now = Math.floor(Date.now() / 1000);
  db.insert(relationships).values({
    sourceType,
    sourceId,
    targetType,
    targetId,
    relationshipType,
    metadata: metadata ?? null,
    createdAt: now,
  }).run();

  return db.select().from(relationships).where(
    and(
      eq(relationships.sourceType, sourceType),
      eq(relationships.sourceId, sourceId),
      eq(relationships.targetType, targetType),
      eq(relationships.targetId, targetId),
      eq(relationships.relationshipType, relationshipType),
    )
  ).get()!;
}

export function getRelatedEntities(
  entityType: string,
  entityId: string,
  relType?: string,
) {
  const outgoingConditions = relType
    ? and(
        eq(relationships.sourceType, entityType),
        eq(relationships.sourceId, entityId),
        eq(relationships.relationshipType, relType),
      )
    : and(
        eq(relationships.sourceType, entityType),
        eq(relationships.sourceId, entityId),
      );

  const incomingConditions = relType
    ? and(
        eq(relationships.targetType, entityType),
        eq(relationships.targetId, entityId),
        eq(relationships.relationshipType, relType),
      )
    : and(
        eq(relationships.targetType, entityType),
        eq(relationships.targetId, entityId),
      );

  const outgoing = db.select().from(relationships).where(outgoingConditions!).all();
  const incoming = db.select().from(relationships).where(incomingConditions!).all();

  return { outgoing, incoming };
}
